    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
    if (ftid.getText().isEmpty() || ftname.getText().isEmpty()
            || ftlevel.getText().isEmpty() || ftability.getText().isEmpty()
            || ftweight.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "All fields must be filled!");
        return;
    }

    int id, level;
    double weight;

    try {
        id = Integer.parseInt(ftid.getText());
        level = Integer.parseInt(ftlevel.getText());
        weight = Double.parseDouble(ftweight.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "ID & Level must be integers\nWeight must be decimal");
        return;
    }


    loadFromFile();

    for (Pokemon p : pokemonList) {
        if (p.getId() == id) {
            JOptionPane.showMessageDialog(this,
                    "Duplicate Pokémon ID not allowed!");
            return;
        }
    }


    pokemonList.add(new Pokemon(
            id,
            ftname.getText(),
            fttype.getSelectedItem().toString(),
            level,
            ftability.getText(),
            weight
    ));

    isSaved = false;

    clearFields();

    JOptionPane.showMessageDialog(this,
            "Pokemon added!\nClick SAVE then LOAD to display.");

    }//GEN-LAST:event_btnaddActionPerformed

    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
           if (pokemonList.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Table is empty, can't save!");
            return;
        }

        if (isSaved) {
            JOptionPane.showMessageDialog(this,
                    "Already saved to pokemon.dat!");
            return;
        }

        saveToFile();
        isSaved = true;

        JOptionPane.showMessageDialog(this,
                "Pokemon list saved successfully!");
    }//GEN-LAST:event_btnsaveActionPerformed

