    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here
                if (pokemonList.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Can't delete because table is empty!");
            return;
        }

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Select a row to delete!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete selected Pokémon?",
                "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) return;

        pokemonList.remove(row);
        saveToFile();
        isSaved = true;

        refreshTable();
        JOptionPane.showMessageDialog(this, "Pokemon deleted!");
    }//GEN-LAST:event_deleteActionPerformed
    
    // SAVE TO FILE 
private void saveToFile() {
        try (ObjectOutputStream oos =
                     new ObjectOutputStream(new FileOutputStream(FILE_NAME))) {
            oos.writeObject(pokemonList);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving file!");
        }
}

    private void loadFromFile() {
        File file = new File(FILE_NAME);
        if (!file.exists()) {
            pokemonList = new ArrayList<>();
            return;
        }
        try (ObjectInputStream ois =
                     new ObjectInputStream(new FileInputStream(FILE_NAME))) {
            pokemonList = (ArrayList<Pokemon>) ois.readObject();
        } catch (Exception e) {
            pokemonList = new ArrayList<>();
        }
    }

        //UTILITIES
    private void refreshTable() {
        DefaultTableModel model =
                (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Pokemon p : pokemonList) {
            model.addRow(new Object[]{
                    p.getId(), p.getName(), p.getType(),
                    p.getLevel(), p.getAbility(), p.getWeight()
            });
        }
    }
// CLEAR INPUT FIELDS
private void clearFields() {
    ftid.setText("");
    ftname.setText("");
    ftlevel.setText("");
    ftweight.setText("");
    ftability.setText("");
    fttype.setSelectedIndex(0);
}

 
